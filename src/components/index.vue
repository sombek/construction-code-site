    //MAGNIFIC POPUP JS
    import 'magnific-popup/dist/jquery.magnific-popup.min.js';
    import 'magnific-popup/dist/magnific-popup.css';

    export default {
        name: 'index', mounted() {
            if (window.innerWidth < 600) {
                this.slider1 = 'assets/images/slider-img/slider1-mobile.jpg';
                this.slider2 = 'assets/images/slider-img/slider2-mobile.jpg';
            } else {
                this.slider1 = 'assets/images/slider-img/slider1.jpg';
                this.slider2 = 'assets/images/slider-img/slider2.jpg';
            }
            this.init();

            window.onresize = () => {
                if (window.innerWidth < 600) {
                    this.slider1 = 'assets/images/slider-img/slider1-mobile.jpg';
                    this.slider2 = 'assets/images/slider-img/slider2-mobile.jpg';
                } else {
                    this.slider1 = 'assets/images/slider-img/slider1.jpg';
                    this.slider2 = 'assets/images/slider-img/slider2.jpg';
                }
                this.initSiteBackgroundImage();
            }
        },
        data() {
            return {
                render: true,
                slider1: '',
                slider2: '',
            }
        },
        methods: {
            init() {
                this.initMainSlider();
                this.initBackgroundImage();
                this.initSiteBackgroundImage();
                this.initWowAnimation();
                this.initAccordian();
                this.windowScroll();
                this.initTestimonialSlider();
                this.initFooterHeight();
                this.initIsotop();
                this.initMagnificPopup();
                this.initScaleImage();
            },
            /*----MAIN ANIMATION-----*/
            initMainSlider() {
                var swiperOptions = {
                    loop: true,
                    speed: 1000,
                    grabCursor: true,
                    watchSlidesProgress: true,
                    mousewheelControl: true,
                    keyboardControl: true,
                    navigation: {
                        nextEl: ".swiper-button-next",
                        prevEl: ".swiper-button-prev"
                    }
                };
                var swiper = new Swiper(".main-slider", swiperOptions);
            },
            /*----BACKGROUND IMAGE-----*/
            initBackgroundImage() {
                $(".bg-image").css('background', function() {
                    var bg = ('url(' + $(this).data("image-src") + ') no-repeat center center');
                    return bg;
                });
            },
            initSiteBackgroundImage() {
                let self = this;
                $(".site-bg-image-1").css('background', function() {
                    var bg = ('url(' + self.slider1 + ') center center / cover no-repeat');
                    return bg;
                })
                $(".site-bg-image-2").css('background', function() {
                    var bg = ('url(' + self.slider2 + ') center center / cover no-repeat');
                    return bg;
                });
            },
            initFooterHeight() {
                var footerHeight = $("footer").outerHeight();
                $('.main-content').css('margin-bottom', footerHeight);
            },
            /*----WOW ANIMATION-----*/
            initWowAnimation() {
                var length = $('.wow').length;
                if (length >= 1) {
                    const wow = new WOW({
                        boxClass: 'wow',
                        animateClass: 'animated',
                        offset: 0,
                    });
                    wow.init();
                }
            },
            /*----ONSCROLL JS-----*/
            windowScroll() {
                var self = this;
                $(window).on("scroll", function() {
                    //self.initCounter();
                });
            },
            /*----ACCORDIAN-----*/
            initAccordian() {
                $(".question-card").on('click', function() {
                    if ($(this).hasClass('active')) {
                        $('.question-card').removeClass('active');
                        $(this).removeClass('active')
                    } else {
                        $('.question-card').removeClass('active');
                        $(this).addClass('active')
                    }
                });
